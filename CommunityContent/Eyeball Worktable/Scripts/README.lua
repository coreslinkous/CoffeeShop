Just a few tips for you:

1.If you put the eyes in a jar or otherwise in glass, you should take off the cornea.
That is the clear bit overtop of the front of the eye. Game engines do weird things when you put a 
semi transparent item behind another semi transparent item.

2. You should remove all the little groups from inside your finished items, they increase file size.
(So adust your eyes in their intended containers, then ungroup the eyes.)

